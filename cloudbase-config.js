// ===================================================================
// CloudBase 配置入口（唯一需要你手动填的文件）
// -------------------------------------------------------------------
// 使用方法：
//   1. 去腾讯云 CloudBase 控制台开一个环境，拿到「环境 ID」
//   2. 把下面的 ENV_ID 从 "" 改成你的环境 ID（形如 "sales-crm-1a2b3c4d"）
//   3. 保存、重新部署即可
//
// 【重要】ENV_ID 留空时 = 演示模式：
//   - 不加载云端，数据/图片全部走浏览器本地（和现在完全一样）
//   - 不需要登录，打开即用
//   一旦填了 ENV_ID，就切换到「云端模式」：数据云端存储、图片对象存储、需登录
// ===================================================================

const CLOUDBASE_CONFIG = {
  // ↓↓↓ 只需要改这一行 ↓↓↓
  ENV_ID: "",   // 例："sales-crm-1a2b3c4d"，留空 = 本地演示模式

  // ---- 以下一般不用改 ----

  // 数据库集合名（在控制台建同名集合即可，文档里有步骤）
  COLLECTION: "customers",

  // 对象存储里图片的存放目录前缀
  STORAGE_DIR: "sales-assets",

  // 登录方式（true 开启）。至少开一种；两个都开则登录页出现两个入口。
  LOGIN: {
    phone:  true,   // 手机号验证码登录
    wechat: false,  // 微信登录（需在控制台额外配公众号/开放平台，较繁琐；演示可先只用手机号）
  },

  // 云端数据同步防抖（毫秒）：连续多次改动只在停手后合并上传一次，省调用次数
  SYNC_DEBOUNCE_MS: 1500,
};

// —— 运行期只读判断，供其它文件使用 ——
const CLOUD_ENABLED = !!(CLOUDBASE_CONFIG.ENV_ID && CLOUDBASE_CONFIG.ENV_ID.trim());

// 本地镜像的 key（云端模式下，每个用户的数据镜像按 uid 隔离）
function cloudMirrorKey(uid) {
  return "tc_sales_cloud_mirror_" + (uid || "anon");
}
